
-- | Поле          | Тип (предварительно)       | Описание / пояснение                          |
-- | ------------- | -------------------------- | --------------------------------------------- |
-- | `id`          | `UUID` / `BIGINT`          | **PK** — уникальный идентификатор узла дерева |
-- | `parent_id`   | `UUID` / `BIGINT`          | **FK** — родительский узел (NULL для корня)   |
-- | `name`        | `TEXT` / `VARCHAR(...)`    | Техническое / проектное наименование          |
-- | `title`       | `TEXT`                     | Понятное имя (для UI)                         |
-- | `params`      | `JSONB`                    | Все технические параметры (height, width, …)  |
-- | `status`      | `TEXT` / `ENUM`            | Статус оборудования («Active», «Archived», …) |
-- | `created_at`  | `TIMESTAMP WITH TIME ZONE` | Когда запись создана                          |
-- | `updated_at`  | `TIMESTAMP WITH TIME ZONE` | Когда запись изменена                         |
-- | `archived_at` | `TIMESTAMP WITH TIME ZONE` | Когда оборудование в архиве                   |
-- | `is_deleted`  | `BOOLEAN`                  | Флаг логического удаления                     |

